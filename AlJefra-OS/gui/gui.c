/* ======================================================================
 * Internal helpers
 * ====================================================================== */

/* Get the draw target (back-buffer if available, else real fb) */
static inline uint32_t *gui_target(void)
{
    return g_use_backbuf ? g_backbuf : g_screen.fb;
}

/* Pitch of draw target in uint32_t units */
static inline int gui_target_pitch(void)
{
    /* Back buffer has pitch = width * 4; real fb uses g_screen.pitch */
    return g_use_backbuf ? g_screen.w : (g_screen.pitch / 4);
}

/* Test if point (px,py) is inside the current clip rectangle */
static inline bool gui_clip_test(int px, int py)
{
    gui_rect_t *c = &g_clip_stack[g_clip_top];
    return px >= c->x && px < c->x + c->w &&
           py >= c->y && py < c->y + c->h;
}

/* Intersect two rectangles; result in *out.  Returns false if empty. */
static bool rect_intersect(gui_rect_t *a, gui_rect_t *b, gui_rect_t *out)
{
    int x1 = a->x > b->x ? a->x : b->x;
    int y1 = a->y > b->y ? a->y : b->y;
    int x2a = a->x + a->w;
    int x2b = b->x + b->w;
    int x2 = x2a < x2b ? x2a : x2b;
    int y2a = a->y + a->h;
    int y2b = b->y + b->h;
    int y2 = y2a < y2b ? y2a : y2b;

    if (x1 >= x2 || y1 >= y2)
        return false;

    out->x = x1;
    out->y = y1;
    out->w = x2 - x1;
    out->h = y2 - y1;
    return true;
}

/* ======================================================================
 * Initialization
 * ====================================================================== */

void gui_init(uint32_t *framebuffer, int width, int height, int pitch)
{
    g_screen.fb    = framebuffer;
    g_screen.w     = width;
    g_screen.h     = height;
    g_screen.pitch = pitch;

    /* Mouse starts at center */
    g_mouse.x = width / 2;
    g_mouse.y = height / 2;
    g_mouse.buttons = 0;
    g_mouse.prev_buttons = 0;
    g_mouse.visible = 1;

    /* Event queue empty */
    g_evt_head = 0;
    g_evt_tail = 0;

    /* Clip to full screen */
    g_clip_top = 0;
    g_clip_stack[0].x = 0;
    g_clip_stack[0].y = 0;
    g_clip_stack[0].w = width;
    g_clip_stack[0].h = height;

    /* Enable back-buffer if resolution fits */
    g_use_backbuf = (width * height <= GUI_BACKBUF_MAX_PIXELS) ? 1 : 0;

    /* Cursor save state */
    g_cursor_saved = 0;

    /* Clear the screen to the primary background */
    gui_draw_rect(0, 0, width, height, GUI_BG);
    if (g_use_backbuf)
        gui_flip();
}

void gui_shutdown(void)
{
    /* Nothing to free -- all static */
}

/* ======================================================================
 * Pixel output
 * ====================================================================== */

void gui_putpixel(int x, int y, uint32_t color)
{
    if (x < 0 || y < 0 || x >= g_screen.w || y >= g_screen.h)
        return;
    if (!gui_clip_test(x, y))
        return;

    uint32_t *dst = gui_target();
    int p = gui_target_pitch();
    dst[y * p + x] = color;
}

/* ======================================================================
 * Rectangle drawing
 * ====================================================================== */

void gui_draw_rect(int x, int y, int w, int h, uint32_t color)
{
    /* Clip against current clip rect */
    gui_rect_t src = {x, y, w, h};
    gui_rect_t clipped;
    if (!rect_intersect(&src, &g_clip_stack[g_clip_top], &clipped))
        return;

    uint32_t *dst = gui_target();
    int p = gui_target_pitch();

    for (int py = clipped.y; py < clipped.y + clipped.h; py++) {
        uint32_t *row = dst + py * p + clipped.x;
        for (int px = 0; px < clipped.w; px++)
            row[px] = color;
    }
}

void gui_draw_rect_border(int x, int y, int w, int h,
                           uint32_t border, uint32_t fill)
{
    /* Fill interior */
    gui_draw_rect(x + 1, y + 1, w - 2, h - 2, fill);
    /* Top edge */
    gui_draw_rect(x, y, w, 1, border);
    /* Bottom edge */
    gui_draw_rect(x, y + h - 1, w, 1, border);
    /* Left edge */
    gui_draw_rect(x, y, 1, h, border);
    /* Right edge */
    gui_draw_rect(x + w - 1, y, 1, h, border);
}

void gui_draw_hline(int x, int y, int w, uint32_t color)
{
    gui_draw_rect(x, y, w, 1, color);
}

void gui_draw_vline(int x, int y, int h, uint32_t color)
{
    gui_draw_rect(x, y, 1, h, color);
}

/* ======================================================================
 * Character / text drawing
 * ====================================================================== */

void gui_draw_char(int x, int y, char c, uint32_t fg, uint32_t bg)
{
    const uint8_t *glyph;
    if (c >= 0x20 && c <= 0x7E)
        glyph = gui_font[c - 0x20];
    else
        glyph = gui_font[0];  /* space for unknown */

    uint32_t *dst = gui_target();
    int p = gui_target_pitch();

    for (int row = 0; row < GUI_FONT_H; row++) {
        int sy = y + row;
        if (sy < 0 || sy >= g_screen.h)
            continue;

        uint8_t bits = glyph[row];
        for (int col = 0; col < GUI_FONT_W; col++) {
            int sx = x + col;
            if (sx < 0 || sx >= g_screen.w)
                continue;
            if (!gui_clip_test(sx, sy))
                continue;

            uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
            dst[sy * p + sx] = color;
        }
    }
}

void gui_draw_char_transparent(int x, int y, char c, uint32_t fg)
{
    const uint8_t *glyph;
    if (c >= 0x20 && c <= 0x7E)
        glyph = gui_font[c - 0x20];
    else
        glyph = gui_font[0];

    uint32_t *dst = gui_target();
    int p = gui_target_pitch();

    for (int row = 0; row < GUI_FONT_H; row++) {
        int sy = y + row;
        if (sy < 0 || sy >= g_screen.h)
            continue;

        uint8_t bits = glyph[row];
        for (int col = 0; col < GUI_FONT_W; col++) {
            int sx = x + col;
            if (sx < 0 || sx >= g_screen.w)
                continue;
            if (!gui_clip_test(sx, sy))
                continue;

            if (bits & (0x80 >> col))
                dst[sy * p + sx] = fg;
        }
    }
}

void gui_draw_text(int x, int y, const char *text, uint32_t color)
{
    if (!text) return;
    int cx = x;
    while (*text) {
        if (*text == '\n') {
            cx = x;
            y += GUI_FONT_H;
        } else {
            gui_draw_char_transparent(cx, y, *text, color);
            cx += GUI_FONT_W;
        }
        text++;
    }
}

int gui_draw_text_wrap(int x, int y, int max_w, const char *text,
                       uint32_t color)
{
    if (!text || max_w <= 0) return 0;

    int cols = max_w / GUI_FONT_W;
    if (cols <= 0) cols = 1;

    int cx = 0;         /* current column */
    int start_y = y;

    while (*text) {
        if (*text == '\n') {
            cx = 0;
            y += GUI_FONT_H;
            text++;
            continue;
        }

        /* Word-wrap: find next word boundary */
        if (cx >= cols) {
            cx = 0;
            y += GUI_FONT_H;
        }

        gui_draw_char_transparent(x + cx * GUI_FONT_W, y, *text, color);
        cx++;
        text++;
    }

    return (y - start_y) + GUI_FONT_H;
}

int gui_measure_text_wrap(int max_w, const char *text)
{
    if (!text || max_w <= 0) return GUI_FONT_H;

    int cols = max_w / GUI_FONT_W;
    if (cols <= 0) cols = 1;

    int cx = 0;
    int lines = 1;

    while (*text) {
        if (*text == '\n') {
            cx = 0;
            lines++;
            text++;
            continue;
        }
        if (cx >= cols) {
            cx = 0;
            lines++;
        }
        cx++;
        text++;
    }

    return lines * GUI_FONT_H;
}

/* ======================================================================
 * Mouse cursor
 * ====================================================================== */

void gui_save_under_cursor(int x, int y)
{
    uint32_t *dst = gui_target();
    int p = gui_target_pitch();
    int idx = 0;

    g_cursor_saved_x = x;
    g_cursor_saved_y = y;
    g_cursor_saved = 1;

    for (int cy = 0; cy < CURSOR_SAVE_H; cy++) {
        int sy = y + cy;
        for (int cx = 0; cx < CURSOR_SAVE_W; cx++) {
            int sx = x + cx;
            if (sx >= 0 && sx < g_screen.w && sy >= 0 && sy < g_screen.h)
                g_cursor_save[idx] = dst[sy * p + sx];
            else
                g_cursor_save[idx] = GUI_BG;
            idx++;
        }
    }
}

void gui_restore_under_cursor(void)
{
    if (!g_cursor_saved) return;

    uint32_t *dst = gui_target();
    int p = gui_target_pitch();
    int idx = 0;

    for (int cy = 0; cy < CURSOR_SAVE_H; cy++) {
        int sy = g_cursor_saved_y + cy;
        for (int cx = 0; cx < CURSOR_SAVE_W; cx++) {
            int sx = g_cursor_saved_x + cx;
            if (sx >= 0 && sx < g_screen.w && sy >= 0 && sy < g_screen.h)
                dst[sy * p + sx] = g_cursor_save[idx];
            idx++;
        }
    }

    g_cursor_saved = 0;
}

void gui_draw_cursor(int x, int y)
{
    uint32_t *dst = gui_target();
    int p = gui_target_pitch();

    for (int cy = 0; cy < CURSOR_H; cy++) {
        int sy = y + cy;
        if (sy < 0 || sy >= g_screen.h) continue;

        for (int cx = 0; cx < CURSOR_W; cx++) {
            int sx = x + cx;
            if (sx < 0 || sx >= g_screen.w) continue;

            uint8_t pixel = cursor_sprite[cy][cx];
            if (pixel == 1)
                dst[sy * p + sx] = GUI_BLACK;
            else if (pixel == 2)
                dst[sy * p + sx] = GUI_WHITE;
            /* 0 = transparent, skip */
        }
    }
}

/* ======================================================================
 * Clipping
 * ====================================================================== */

void gui_push_clip(gui_rect_t *r)
{
    if (g_clip_top >= GUI_CLIP_STACK_MAX - 1)
        return;  /* Stack full */

    gui_rect_t clipped;
    if (rect_intersect(r, &g_clip_stack[g_clip_top], &clipped)) {
        g_clip_top++;
        g_clip_stack[g_clip_top] = clipped;
    } else {
        /* Empty clip -- push a zero-area rect so pop works */
        g_clip_top++;
        g_clip_stack[g_clip_top].x = 0;
        g_clip_stack[g_clip_top].y = 0;
        g_clip_stack[g_clip_top].w = 0;
        g_clip_stack[g_clip_top].h = 0;
    }
}

void gui_pop_clip(void)
{
    if (g_clip_top > 0)
        g_clip_top--;
}

void gui_reset_clip(void)
{
    g_clip_top = 0;
    g_clip_stack[0].x = 0;
    g_clip_stack[0].y = 0;
    g_clip_stack[0].w = g_screen.w;
    g_clip_stack[0].h = g_screen.h;
}

/* ======================================================================
 * Double buffering
 * ====================================================================== */

void gui_flip(void)
{
    if (!g_use_backbuf)
        return;

    /* Copy back buffer to real framebuffer, line by line */
    int fb_pitch = g_screen.pitch / 4;  /* uint32_t units */
    uint32_t *fb = g_screen.fb;

    for (int y = 0; y < g_screen.h; y++) {
        uint32_t *src_row = g_backbuf + y * g_screen.w;
        uint32_t *dst_row = fb + y * fb_pitch;
        for (int x = 0; x < g_screen.w; x++)
            dst_row[x] = src_row[x];
    }
}

/* ======================================================================
 * Event queue
 * ====================================================================== */

void gui_push_event(gui_event_t *evt)
{
    int next = (g_evt_head + 1) % GUI_EVT_QUEUE_SIZE;
    if (next == g_evt_tail)
        return;  /* Queue full -- drop oldest would be another policy */

    g_evt_queue[g_evt_head] = *evt;
    g_evt_head = next;
}

bool gui_poll_event(gui_event_t *evt)
{
    if (g_evt_head == g_evt_tail)
        return false;

    *evt = g_evt_queue[g_evt_tail];
    g_evt_tail = (g_evt_tail + 1) % GUI_EVT_QUEUE_SIZE;
    return true;
}

