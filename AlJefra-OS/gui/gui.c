int gui_strlen(const char *s)
{
    int n = 0;
    if (!s) return 0;
    while (s[n]) n++;
    return n;
}

void gui_strncpy(char *dst, const char *src, int max)
{
    int i;
    if (!dst || !src || max <= 0) return;
    for (i = 0; i < max - 1 && src[i]; i++)
        dst[i] = src[i];
    dst[i] = '\0';
}

void gui_itoa(int val, char *buf, int bufsz)
{
    if (!buf || bufsz <= 0) return;

    if (bufsz == 1) {
        buf[0] = '\0';
        return;
    }

    int neg = 0;
    unsigned int uval;

    if (val < 0) {
        neg = 1;
        uval = (unsigned int)(-(val + 1)) + 1u;
    } else {
        uval = (unsigned int)val;
    }

    /* Build digits in reverse */
    char tmp[16];
    int len = 0;

    if (uval == 0) {
        tmp[len++] = '0';
    } else {
        while (uval > 0 && len < 15) {
            tmp[len++] = '0' + (char)(uval % 10);
            uval /= 10;
        }
    }

    int pos = 0;
    if (neg && pos < bufsz - 1)
        buf[pos++] = '-';

    for (int i = len - 1; i >= 0 && pos < bufsz - 1; i--)
        buf[pos++] = tmp[i];

    buf[pos] = '\0';
}
